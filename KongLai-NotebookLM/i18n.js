export function normalizeLocale(locale) {
  const value = String(locale || "").toLowerCase();
  if (value.startsWith("zh")) return "zh-CN";
  return "en";
}

export async function getLocale() {
  const saved = await chrome.storage.local.get(LOCALE_KEY);
  if (saved && saved[LOCALE_KEY]) return normalizeLocale(saved[LOCALE_KEY]);
  return normalizeLocale(globalThis.navigator?.language || "zh-CN");
}

export async function setLocale(locale) {
  const normalized = normalizeLocale(locale);
  await chrome.storage.local.set({ [LOCALE_KEY]: normalized });
  return normalized;
}

function findByKey(locale, key) {
  const dict = DICT[locale] || DICT.en;
  return key.split(".").reduce((obj, part) => (obj && obj[part] !== undefined ? obj[part] : undefined), dict);
}

export function t(locale, key, vars = {}) {
  const normalized = normalizeLocale(locale);
  const raw = findByKey(normalized, key) ?? findByKey("en", key) ?? key;
  return String(raw).replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
}

export function applyI18n(locale, root = document) {
  const normalized = normalizeLocale(locale);
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(normalized, node.dataset.i18n || "");
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(normalized, node.dataset.i18nPlaceholder || ""));
  });
  root.querySelectorAll("[data-i18n-title]").forEach((node) => {
    node.setAttribute("title", t(normalized, node.dataset.i18nTitle || ""));
  });
}

export function localizeResult(locale, resultKey) {
  return t(locale, `common.result.${resultKey || "idle"}`);
}

export function localizeMode(locale, modeKey) {
  return t(locale, `common.mode.${modeKey || "system"}`);
}

export function fillLocaleSelect(select, locale) {
  if (!select) return;
  const normalized = normalizeLocale(locale);
  select.innerHTML = LOCALE_OPTIONS
    .map((item) => `<option value="${item.value}" ${item.value === normalized ? "selected" : ""}>${item.label}</option>`)
    .join("");
}
